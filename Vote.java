package entites;

/**
 *
 * @author Abbes
 */
public class Vote {
    
    private int id , id_user,id_post,type;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId_user() {
        return id_user;
    }

    public void setId_user(int id_user) {
        this.id_user = id_user;
    }

    public int getId_post() {
        return id_post;
    }

    public void setId_post(int id_post) {
        this.id_post = id_post;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return "Vote{" + "id=" + id + ", id_user=" + id_user + ", id_post=" + id_post + ", type=" + type + '}';
    }
    
    
    
    
}
